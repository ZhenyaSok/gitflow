def sring_upper():
    """функция возвращает слово, ккаждая буква становится заглавной и эта функция точно лучше!!!!!"""
    string = str(input())
    return string.upper()


def sring_title():
    """Функция, которая возвращает слово/фразу, первая буква каждого слова становится заглавной"""
    string = str(input())
    return string.title()
